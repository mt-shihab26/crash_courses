fn main() {
    let x: i32 = -1123; // i8, i16, i32, i64, i128, isize, u8, u16, u32, u64, u128, usize, 
    let y: f64 = 123.123; // f32, f64
    let z: bool = true;
    let c: char = 'a';

    println!("Integer: {}", x);
    println!("Floating: {}", y);
    println!("Boolean: {}", z);
    println!("Character: {}", c);
}
